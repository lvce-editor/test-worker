import assert from 'node:assert/strict'
import test from 'node:test'
import { getActualApiTypesContent } from '../src/getActualApiTypesContent.ts'

test('getActualApiTypesContent preserves multi-line exported object type aliases', () => {
  const contentApi = `// Generated by dts-bundle-generator v9.5.1

export type LayoutExpectationValue = string | LayoutExpectationObject;
export type LayoutExpectationObject = {
\treadonly [key: string]: LayoutExpectationValue | undefined;
};
export interface LayoutExpectation extends LayoutExpectationObject {
\treadonly activeGroupIndex?: number;
}
export type SavedState = {
\treadonly layout?: LayoutExpectationObject;
};
declare const shouldHaveLayout: (expectedLayout: LayoutExpectation, uid?: number) => Promise<void>;

declare namespace Main {
\texport { shouldHaveLayout };
}
`

  const actual = getActualApiTypesContent(contentApi, 'export {}\n', 'export {}\n')

  assert.match(actual, /export type LayoutExpectationObject = \{\n\treadonly \[key: string\]: LayoutExpectationValue \| undefined;\n\};/)
  assert.match(actual, /export type SavedState = \{\n\treadonly layout\?: LayoutExpectationObject;\n\};/)
  assert.match(actual, /interface Main \{\n  readonly shouldHaveLayout: \(expectedLayout: LayoutExpectation, uid\?: number\) => Promise<void>;\n\}/)
})

test('getActualApiTypesContent removes locator implementation details', () => {
  const contentApi = `// Generated by dts-bundle-generator v9.5.1

declare namespace Main {
}
`
  const contentLocator = `// Generated by dts-bundle-generator v9.5.1

export interface LocatorClickOptions {
\treadonly button?: string;
}
export interface ILocatorExternal {
\treadonly click: (options?: LocatorClickOptions) => Promise<void>;
\treadonly dispatchEvent: (type: string, init: string) => Promise<void>;
\treadonly first: () => ILocatorExternal;
\treadonly hover: () => Promise<void>;
\treadonly locator: (subSelector: string) => ILocatorExternal;
\treadonly nth: (nth: number) => ILocatorExternal;
\treadonly type: (text: string) => Promise<void>;
}
export interface ParsedCssSelectorPart {
\treadonly selector: string;
\treadonly type: "css";
}
export interface ParsedTextSelectorPart {
\treadonly text: string;
\treadonly type: "text";
}
export interface ParsedHasTextSelectorPart {
\treadonly text: string;
\treadonly type: "has-text";
}
export interface ParsedNthSelectorPart {
\treadonly index: number;
\treadonly type: "nth";
}
export type ParsedSelectorPart = ParsedCssSelectorPart | ParsedTextSelectorPart | ParsedHasTextSelectorPart | ParsedNthSelectorPart;
export type ParsedCssSelector = readonly ParsedSelectorPart[];
export interface ILocatorInternal {
\treadonly _parsed: ParsedCssSelector;
\treadonly _selector: string;
}
export interface ILocator extends ILocatorInternal, ILocatorExternal {
}
export interface ILocatorCreateOptions {
\treadonly hasText?: string;
\treadonly nth?: number;
}
export declare class Locator implements ILocator {
\treadonly _selector: string;
\treadonly _parsed: ParsedCssSelector;
\tconstructor(selector: string, options?: ILocatorCreateOptions, parsed?: ParsedCssSelector);
\tclick({ button }?: {
\t\treadonly button?: string;
\t}): Promise<void>;
\thover(): Promise<void>;
\tfirst(): any;
\tlocator(subSelector: string): any;
\tnth(nth: number): any;
\ttype(text: string): Promise<void>;
\tdispatchEvent(type: string, init: any): Promise<void>;
}

export {};
`

  const actual = getActualApiTypesContent(contentApi, 'export {}\n', contentLocator)

  assert.match(actual, /interface LocatorClickOptions \{\n\treadonly button\?: string;\n\}/)
  assert.match(actual, /interface ILocatorExternal \{/)
  assert.match(actual, /interface LocatorOptions \{\n  readonly hasText\?: string\n  readonly nth\?: number\n\}/)
  assert.match(actual, /readonly Locator: \(selector: string, options\?: LocatorOptions\) => ILocatorExternal/)

  assert.doesNotMatch(actual, /declare class Locator/)
  assert.doesNotMatch(actual, /ILocatorInternal/)
  assert.doesNotMatch(actual, /interface ILocator /)
  assert.doesNotMatch(actual, /ILocatorCreateOptions/)
  assert.doesNotMatch(actual, /ParsedCssSelector/)
  assert.doesNotMatch(actual, /_selector/)
  assert.doesNotMatch(actual, /_parsed/)
})
